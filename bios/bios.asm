; bios_func_table[bios_func_num](...)
; this is at 0x1600, bios functions use the standard ABI
bios_func_table:
	dd draw_glyph
	dd locate_glyph
	dd find_pixel
	dd fill_screen
	dd print_str
	dd print_str32

bios_boot_message: db "RoadRisc-32 PC BIOS   V 1.0   2026/6/17", 0x00
align 4
bios_copyright_message: db "(C) Lemon 2026", 0x00
align 4
bios_memcheck_mesage: db "Kb Okay", 0x00
align 4
bios_cpuver_mesage: db "CPU Ver", 0x00
align 4
bios_uart_message: db "Waiting for UART packets...", 0x00
align 4
bios_comptime_message: db "Assemble date ", ASSEMBLE_TIME, 0x00
align 4

extern bios_entry
bios_entry:
	mov r0, STACK_TOP
	mov sp, r0

	mov r0, VGA_PALETTE_BASE
	mov r1, 0x0080
	mov [r0], r1

	call probe_memory
	mov r8, r0

	mov r0, 0x0
	call fill_screen

	xor r0, r0
	xor r1, r1
	mov r2, bios_boot_message
	call print_str

	xor r0, r0
	mov r1, 8
	mov r2, bios_copyright_message
	call print_str

	mov r2, r8
	xor r0, r0
	mov r1, 40
	shl r2, 2
	sub r2, 10752
	call print_number
	add r0, 8
	mov r2, bios_memcheck_mesage
	call print_str

	xor r0, r0
	mov r1, 232
	mov r2, bios_cpuver_mesage
	call print_str
	add r0, 8
	mov r8, RRISC_VERSION_ADDR_HIGH
	shl r8, 16
	or r8, RRISC_VERSION_ADDR_LOW
	mov r8, [r8]
	shr r2, r8, 16
	and r2, 0x7fff
	call print_number
	mov r4, r0
	mov r2, '.'
	call draw_glyph
	mov r0, r4
	add r0, 8
	and r2, r8, 0xffff
	call print_number

	xor r0, r0
	mov r1, 224
	mov r2, bios_comptime_message
	call print_str

	xor r0, r0
	mov r1, 80
	mov r2, bios_uart_message
	call print_str

	mov r0, UART_STAT
	mov r1, UART_RX
	mov r2, UART_STAT_RX_READY
	xor r3, r3
	xor r6, r6
bios_entry.poll_uart:
	mov r4, [r0]
	and r4, r2
	beq r4, r2, bios_entry.load_program
	jmp bios_entry.poll_uart

bios_entry.load_program:
	mov r5, [r1]
bios_entry.load_program.poll_uart:
	mov r4, [r0]
	and r4, r2
	bneq r4, r2, bios_entry.load_program.poll_uart
bios_entry.load_program.read_word:
	mov r7, [r1]
	mov [r3], r7
	add r6, 1
	add r3, 1
	bneq r5, r6, bios_entry.load_program.poll_uart

bios_entry.load_program.detect:
bios_entry.load_program.detect.roadrun:
	xor r3, r3
	mov r8, EXECUTABLE_ROADRUN
	mov r7, [r3]
	mov r9, 0x7f52
	shl r9, 16
	or r9, 0x4f41
	bneq r7, r9, bios_entry.load_program.detect.bin
	mov r7, [r3 + 1]
	mov r9, 0x4452
	shl r9, 16
	or r9, 0x554e
	bneq r7, r9, bios_entry.load_program.detect.bin
	jmp bios_entry.load_program.enter

bios_entry.load_program.detect.bin:
	mov r8, EXECUTABLE_BIN

bios_entry.load_program.enter:
	mov r10, bios_entry
	push r10

	mov r10, bios_entry.load_program.enter.jmptable
	add r10, r8
	mov r10, [r10]
	push r10
	ret

bios_entry.load_program.enter.jmptable:
	dd bios_entry.load_program.enter.bin
	dd bios_entry.load_program.enter.roadrun

bios_entry.load_program.enter.bin:
	xor r0, r0
	mov r1, VGA_PALETTE_BASE
	mov [r1], r0
	call fill_screen
	jmpabs 0x0000

bios_entry.load_program.enter.roadrun:
	xor r0, r0
	mov r1, VGA_PALETTE_BASE
	mov [r1], r0
	call fill_screen
	mov r3, [r3 + 3] ; push header->entry_point
	shr r3, 2
	push r3

	xor r3, r3
	mov r3, [r3 + 2] ; r0 = vmembase + (size - 1), r3 = vmembase
	shr r3, 2
	add r0, r3, r6
	sub r0, 5

	xor r4, r4 ; r1 = blob + (size - 1), r4 = header + sizeof(header)
	add r1, r4, r6
	sub r1, 1
	add r4, 4

	blt r1, r0, bios_entry.load_program.enter.roadrun.relocate_forward

	xor r2, r2
bios_entry.load_program.enter.roadrun.relocate_backward:
	mov r5, [r1]
	mov [r0], r5
	sub r0, 1
	sub r1, 1
	bneq r1, r2, bios_entry.load_program.enter.roadrun.relocate_backward
	ret

bios_entry.load_program.enter.roadrun.relocate_forward:
	mov r5, [r4]
	mov [r3], r5
	add r3, 1
	add r4, 1
	blt r6, r3, bios_entry.load_program.enter.roadrun.relocate_forward
	ret



probe_memory: ; probe_memory()
	mov r0, 0x4000
probe_memory.loop:
	mov r13, 0x5555
	shl r13, 16
	or r13, 0x5555
	mov r14, 0xaaaa
	shl r14, 16
	or r14, 0xaaaa
	mov [r0], r13
	mov [r0 + 1], r14
	mov r15, [r0]
	bneq r13, r15, probe_memory.exit
	mov r15, [r0 + 1]
	bneq r14, r15, probe_memory.exit
	add r0, 0x1000
	jmp probe_memory.loop
probe_memory.exit:
	ret
	


fill_screen: ; fill_screen(colour) fill screen
	mov r15, 0x1111
	shl r15, 16
	or r15, 0x1111
	and r13, r0, 0xf
	mul r15, r13
	mov r13, VGA_FB
	mov r14, VGA_FB_END
fill_screen.loop:
	mov [r13], r15
	add r13, 1
	blt r14, r13, fill_screen.loop
	ret



print_number: ; print_number(x, y, n) - print number
	push r2
	push r3
	xor r3, r3
print_number.print_digit:
	shr r13, r2, 1
	shr r14, r2, 2
	add r13, r14
	shr r14, r13, 4
	add r13, r14
	shr r14, r13, 8
	add r13, r14
	shr r14, r13, 16
	add r13, r14
	shr r13, 3
	shl r15, r13, 2
	add r15, r13
	shl r15, 1
	sub r15, r2, r15
	add r15, 6
	shr r15, 4
	add r13, r15

	mov r15, 10
	mul r14, r13, r15
	sub r14, r2, r14
	add r3, 1
	push r14
	xor r15, r15
	mov r2, r13
	bneq r13, r15, print_number.print_digit

print_number.print_loop:
	pop r2
	push r0
	add r2, '0'
	call draw_glyph
	pop r0
	add r0, 8
	sub r3, 1
	xor r15, r15
	bneq r3, r15, print_number.print_loop

	pop r3
	pop r2
	ret



print_str: ; print_str(x, y, s)
	push r5
	push r4
	push r3
	mov r13, r2
	xor r15, r15

print_str.readloop:
	mov r14, [r13]

	xor r3, r3
	sub r3, 8
	mov r4, 24
print_str.shiftloop:
	shr r5, r14, r4
	and r5, 0xff
	beq r5, r15, print_str.exit
	sub r4, 8

	push r0
	push r13
	push r14
	push r15
	mov r2, r5
	call draw_glyph
	pop r15
	pop r14
	pop r13
	pop r0
	add r0, 8

	bneq r3, r4, print_str.shiftloop
	add r13, 1
	jmp print_str.readloop

print_str.exit:
	pop r3
	pop r4
	pop r5
	ret



print_str32: ; print_str32(x, y, s, len)
	push r2

	add r13, r2, r3
	mov r14, r2
print_str32.loop:
	mov r2, [r14]
	add r14, 1

	push r0
	push r13
	push r14
	call draw_glyph
	pop r14
	pop r13
	pop r0

	add r0, 8
	blt r13, r14, print_str32.loop	

	pop r2
	ret



find_pixel: ; find_pixel(x, y) - return pixel address in r0
	mov r15, VGA_WIDTH
	mul r15, r1, r15
	add r0, r15
	shr r0, 3
	add r0, VGA_FB
	ret



draw_word: ; draw_word(glyph, x, y, type)
	push r0
	push r0
	mov r0, r1
	mov r1, r2
	mov r13, r0
	call find_pixel
	mov r14, r0
	mov r1, r13
	pop r0

	mov r13, draw_word.exit ; risc-maxx `call reg`
	push r13
	add r13, r3, draw_word.jmptable
	mov r13, [r13]
	push r13
	mov r15, [r0]
	ret

draw_word.exit:
	pop r0
	add r0, 1
	ret

draw_word.jmptable:
	dd draw_word.bitmap_glyph
	dd draw_word.raw_glyph

draw_word.raw_glyph:
	mov r13, [r14]
	or r13, r15
	mov [r14], r13
	add r2, 1
	ret

draw_word.bitmap_glyph:
	push r12
	mov r0, r15

	shr r15, r0, 24
	call draw_word.bitmap_glyph.broadcast_pixel

	shr r15, r0, 16
	add r14, VGA_STRIDE
	call draw_word.bitmap_glyph.broadcast_pixel

	shr r15, r0, 8
	add r14, VGA_STRIDE
	call draw_word.bitmap_glyph.broadcast_pixel

	mov r15, r0
	add r14, VGA_STRIDE
	call draw_word.bitmap_glyph.broadcast_pixel
	pop r12
	add r2, 4
	ret

draw_word.bitmap_glyph.broadcast_pixel:
	and r15, 0xff
	shl r13, r15, 12 ; pass 1
	or r15, r13
	mov r12, 0x000f
	shl r12, 16
	or r12, 0x000f
	and r15, r12

	shl r13, r15, 6 ; pass 2
	or r15, r13
	mov r12, 0x0303
	shl r12, 16
	or r12, 0x0303
	and r15, r12

	shl r13, r15, 3 ; pass 3
	or r15, r13
	mov r12, 0x1111
	shl r12, 16
	or r12, 0x1111
	and r15, r12

	mov r12, 0xf
	mul r15, r12

	mov r12, [r14]
	or r12, r15
	mov [r14], r12
	ret



draw_glyph: ; draw_glyph(x, y, c)
	push r1
	push r2
	push r3
	push r4

	mov r15, r0
	push r15
	mov r0, r2
	call locate_glyph

	pop r15
	xor r4, r4
	beq r0, r4, graw_glyph.fail

	mov r4, [r0]
	and r3, r4, 0xffff

	add r14, r3, font_size_table
	mov r14, [r14]
	add r4, r0, r14

	add r0, 1
	mov r2, r1
	mov r1, r15

 draw_glyph.draw_loop:
	call draw_word
	bneq r0, r4, draw_glyph.draw_loop

draw_glyph.exit:
	xor r0, r0
	pop r4
	pop r3
	pop r2
	pop r1
	ret

graw_glyph.fail:
	pop r0
	mov r0, 1
	jmp draw_glyph.exit

locate_glyph: ; locate_glyph(c) - find glyph in font table, return null if not found
	push r1

	mov r1, 0x7f
	blt r1, r0, locate_glyph.flat

	mov r15, r0
	mov r13, font_table_end
	mov r0, font_table_flat_end
locate_glyph.readloop:
	mov r1, [r0]
	shr r14, r1, 16
	beq r14, r15, locate_glyph.exit

	and r1, r1, 0xffff
	add r14, r1, font_size_table
	mov r14, [r14]

	add r0, r14
	bneq r0, r13, locate_glyph.readloop

locate_glyph.fail:
	xor r0, r0

locate_glyph.exit:
	pop r1
	ret

locate_glyph.flat:
	mov r1, 0x20
	blt r1, r0, locate_glyph.fail
	mov r1, 3
	sub r0, 0x20
	mul r0, r1
	add r0, font_table

	pop r1
	ret

