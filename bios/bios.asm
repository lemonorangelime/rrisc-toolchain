extern find_pixel
find_pixel: ; find_pixel(x, y) - return pixel address in r0
	mov r15, 320
	mul r15, r1, r15
	add r0, r15
	shr r0, 3
	add r0, VGA_FB
	ret

extern draw_word
draw_word: ; draw_word(glyph, x, y, type)
	push r0
	push r0
	mov r0, r1
	mov r1, r2
	mov r13, r0
	call find_pixel
	mov r14, r0
	mov r1, r13
	pop r0

	mov r13, draw_word.exit ; risc-maxx `call reg`
	push r13
	add r13, r3, draw_word.jmptable
	mov r13, [r13]
	push r13
	mov r15, [r0]
	ret

draw_word.exit:
	pop r0
	add r0, 1
	ret

draw_word.jmptable:
	dd draw_word.bitmap_glyph
	dd draw_word.raw_glyph

draw_word.raw_glyph:
	mov r13, [r14]
	or r13, r15
	mov [r14], r13
	add r2, 1
	ret

draw_word.bitmap_glyph:
	push r12
	mov r0, r15

	shr r15, r0, 24
	call draw_word.broadcast_pixel

	shr r15, r0, 16
	add r14, VGA_STRIDE
	call draw_word.broadcast_pixel

	shr r15, r0, 8
	add r14, VGA_STRIDE
	call draw_word.broadcast_pixel

	mov r15, r0
	add r14, VGA_STRIDE
	call draw_word.broadcast_pixel
	pop r12
	add r2, 4
	ret

draw_word.broadcast_pixel:
	and r15, 0xff
	shl r13, r15, 12 ; pass 1
	or r15, r13
	mov r12, 0x000f
	shl r12, 16
	or r12, 0x000f
	and r15, r12

	shl r13, r15, 6 ; pass 2
	or r15, r13
	mov r12, 0x0303
	shl r12, 16
	or r12, 0x0303
	and r15, r12

	shl r13, r15, 3 ; pass 3
	or r15, r13
	mov r12, 0x1111
	shl r12, 16
	or r12, 0x1111
	and r15, r12

	mov r12, 0xf
	mul r15, r12

	mov r12, [r14]
	or r12, r15
	mov [r14], r12
	ret

extern draw_glyph
draw_glyph: ; draw_glyph(x, y, c)
	push r1
	push r2
	push r3
	push r4

	mov r15, r0
	push r15
	mov r0, r2
	call locate_glyph

	pop r15
	xor r4, r4
	beq r0, r4, graw_glyph.fail

	mov r4, [r0]
	and r3, r4, 0xffff

	add r14, r3, font_size_table
	mov r14, [r14]
	add r4, r0, r14

	add r0, 1
	mov r2, r1
	mov r1, r15

 draw_glyph.draw_loop:
	call draw_word
	bneq r0, r4, draw_glyph.draw_loop

draw_glyph.exit:
	xor r0, r0
	pop r4
	pop r3
	pop r2
	pop r1
	ret

graw_glyph.fail:
	pop r0
	mov r0, 1
	jmp draw_glyph.exit

extern locate_glyph
locate_glyph: ; locate_glyph(c) - find glyph in font table, return null if not found
	push r1

	mov r15, r0
	mov r13, font_table_end
	mov r0, font_table
locate_glyph.readloop:
	mov r1, [r0]
	shr r14, r1, 16
	beq r14, r15, locate_glyph.exit

	and r1, r1, 0xffff
	add r14, r1, font_size_table
	mov r14, [r14]

	add r0, r14
	bneq r0, r13, locate_glyph.readloop

locate_glyph.exit:
	pop r1
	ret

